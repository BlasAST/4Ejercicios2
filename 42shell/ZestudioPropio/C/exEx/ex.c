#include <unistd.h>

int main(int args , char **argv)
{
	int i;

	i = 0;
	while (argv[1][i] != '\0')
	{
		write(1,&(argv[1][i]),1);
		i++;
	}
	i = 0;
	while(argv[1][i] != '\0')
	{
		if (argv[1][i] == 'u')
		{
			write(1, "\nu\n",3);
		}
		i++;
	}
}
